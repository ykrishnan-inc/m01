def teach_math():
    """Interactive math lesson for beginners"""
    print("\nMath Lesson:")
    print("Let's learn some basic math operations!")
    x = 10
    y = 5
    print(f"x = {x}, y = {y}")
    print(f"x + y = {x + y}")
    print(f"x - y = {x - y}")
    print(f"x * y = {x * y}")
    print(f"x / y = {x / y}")
    input("Press Enter to continue...")
